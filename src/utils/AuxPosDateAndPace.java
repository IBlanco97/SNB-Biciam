package utils;

public class AuxPosDateAndPace {

    private int posDate;
    private int posPlace;

    public AuxPosDateAndPace(int posDate, int posPlace) {
        this.posDate = posDate;
        this.posPlace = posPlace;
    }

    public int getPosDate() {
        return posDate;
    }

    public void setPosDate(int posDate) {
        this.posDate = posDate;
    }

    public int getPosPlace() {
        return posPlace;
    }

    public void setPosPlace(int posPlace) {
        this.posPlace = posPlace;
    }
}
